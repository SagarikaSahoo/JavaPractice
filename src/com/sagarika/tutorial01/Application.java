package com.sagarika.tutorial01;

public class Application {

	public static void main(String[] args) {
		int number = 5;
		short number1 = 25;
		long num2 = 1234;

		char num3 = 'A';
		double num4 = 5.00;
		float num5 = 20.32f;

		System.out.println("The integer number " + number);
		System.out.println("The short number " + number1);
		System.out.println("The long number " + num2);
		System.out.println("The char number " + num3);
		System.out.println("The double number " + num4);
		System.out.println("The float number " + num5);
	}

}

